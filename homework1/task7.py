#Напишите программу, которая принимает на вход температуру в градусах Цельсия и переводит ее в градусы Фаренгейта или наоборот, в зависимости от выбора пользователя.

def temperature_converter():
    print("Программа для перевода температур")
    
    # Выбор пользователем направления конверсии
    choice = input("Введите 'C' для перевода из Цельсия в Фаренгейт или 'F' для перевода из Фаренгейта в Цельсий: ").strip().upper()
    
    if choice == 'C':
        # Ввод температуры в градусах Цельсия
        celsius = float(input("Введите температуру в градусах Цельсия: "))
        # Перевод в Фаренгейт
        fahrenheit = (celsius * 9/5) + 32
        print(f"{celsius}°C = {fahrenheit}°F")
    elif choice == 'F':
        # Ввод температуры в градусах Фаренгейта
        fahrenheit = float(input("Введите температуру в градусах Фаренгейт: "))
        # Перевод в Цельсий
        celsius = (fahrenheit - 32) * 5/9
        print(f"{fahrenheit}°F = {celsius}°C")
    else:
        print("Ошибка: Неверный выбор. Пожалуйста, введите 'C' или 'F'.")

# Запуск конвертера
temperature_converter()